void solve() {
    int n;
    cin >> n;
    vector<int> ele(n, 0);
    for(int i = 0; i < n; i++){
    	cin >> ele[i];
    }
    for(int i = 0; i < n; i++){
    	int m = 15;
    	for(int j = 0; j <= 15; j++){
    		for(int k = 0; k <= 15; k++){
    			ll val = 0;
    			val = ele[i] + j;
    			val *= (long long)pow(2, k);
    			if(val % 32768 == 0){
    				m = min(m, j + k);
    			}
    		}
    	}
    	cout << m << " ";
    }
    cout << "\n";
}


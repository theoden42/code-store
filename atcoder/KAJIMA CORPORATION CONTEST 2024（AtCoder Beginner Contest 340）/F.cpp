int gcd(int a, int b, int& x, int& y) {
    if (b == 0) {
        x = 1;
        y = 0;
        return a;
    }
    int x1, y1;
    int d = gcd(b, a % b, x1, y1);
    x = y1;
    y = x1 - y1 * (a / b);
    return d;
}


void solve() {
   int x, y;
   cin >> x >> y;
	
   int ax = abs(x);
   int ay = abs(y);

   int a, b;
   int g = gcd(ay, ax, a, b);

   if(g > 2){
        cout << "-1\n";
        return;
   }

   a *= 2 / g;
   b *= 2 / g;

   if(x < 0){
      b *= -1;
   } 
   if(y > 0){
     a *= -1;
   }

   cout << a << " " << b << "\n";

}



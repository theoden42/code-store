long long compute_hash(string const& s) {
    const int p = 31;
    const int m = 1e9 + 9;
    long long hash_value = 0;
    long long p_pow = 1;
    for (char c : s) {
        hash_value = (hash_value + (c - 'a' + 1) * p_pow) % m;
        p_pow = (p_pow * p) % m;
    }
    return hash_value;
}

void solve() {
    int n;
    cin >> n;
    set<int> st;

    for(int i = 0; i < n; i++){
    	string x;
    	cin >> x;
    	int h1 = compute_hash(x);
    	reverse(all(x));
    	int h2 = compute_hash(x);

    	if(st.find(h1) != st.end() || st.find(h2) != st.end())
    		continue;

    	st.insert(h1);
    }

    cout << st.size() << "\n";
}



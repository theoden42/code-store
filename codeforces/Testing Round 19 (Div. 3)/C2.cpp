vector<int> z_function(string s) {
    int n = s.size();
    vector<int> z(n);
    int l = 0, r = 0;
    for(int i = 1; i < n; i++) {
        if(i < r) {
            z[i] = min(r - i, z[i - l]);
        }
        while(i + z[i] < n && s[z[i]] == s[i + z[i]]) {
            z[i]++;
        }
        if(i + z[i] > r) {
            l = i;
            r = i + z[i];
        }
    }
    return z;
}

void solve() {
    string s;
    cin >> s;
    int n = s.size();
    vector<int> zarr = z_function(s);
    for(int i = (n - 1) / 2; i >= 0; i--){
    	int x = zarr[i];
    	if(x == n - i){
    		cout << "YES\n";
    		cout << s.substr(i);
    		return;
    	}
    }
    cout << "NO\n";
}


